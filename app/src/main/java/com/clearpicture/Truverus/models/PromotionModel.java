package com.clearpicture.Truverus.models;

public class PromotionModel {
    private int promoImg;
    private String promotionName;
    private String descount;

    public int getPromoImg() {
        return promoImg;
    }

    public void setPromoImg(int promoImg) {
        this.promoImg = promoImg;
    }

    public String getPromotionName() {
        return promotionName;
    }

    public void setPromotionName(String promotionName) {
        this.promotionName = promotionName;
    }

    public String getDescount() {
        return descount;
    }

    public void setDescount(String descount) {
        this.descount = descount;
    }
}
